// FilterResult provides detailed information about why a file was or wasn't filtered.
// Returned by FilterDetailed and related methods for structured result inspection.
type FilterResult struct {
	Filtered bool
	Reason   FilterReason
	Path     string
	Trace    string
}

// Is reports whether this result was filtered with the given reason.
// Returns false if the file was not filtered or was filtered for a different reason.
func (r FilterResult) Is(reason FilterReason) bool {
	return r.Filtered && r.Reason == reason
}

// String returns a human-readable representation of the filter result.
func (r FilterResult) String() string {
	if !r.Filtered {
		return "FilterResult(filtered=false)"
	}

	if r.Trace != "" {
		return "FilterResult(filtered=true, reason=" + string(r.Reason) + ", trace=" + r.Trace + ")"
	}

	return "FilterResult(filtered=true, reason=" + string(r.Reason) + ")"
}

