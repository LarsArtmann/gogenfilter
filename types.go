package gogenfilter

import "slices"

// FilterOption represents a type of generated code to filter.
type FilterOption string

// String returns the filter option as a string.
func (o FilterOption) String() string { return string(o) }

// Reason returns the FilterReason corresponding to this FilterOption.
// Returns (reason, true) for detector options (FilterSQLC, FilterTempl, etc.).
// Returns ("", false) for meta-options (FilterAll) or unregistered options.
// Derived from the detectors table so adding a new detector automatically
// updates this mapping.
func (o FilterOption) Reason() (FilterReason, bool) {
	return filterOptionToReason(o)
}

// IsValid reports whether the FilterOption is a recognized value.
func (o FilterOption) IsValid() bool {
	return slices.Contains(AllFilterOptions(), o)
}

