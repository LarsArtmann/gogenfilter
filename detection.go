// filterOptionToReason maps FilterOption values to their FilterReason using the
// detectors table. Returns (reason, true) for registered options, ("", false) otherwise.
func filterOptionToReason(opt FilterOption) (FilterReason, bool) {
	for _, d := range detectors {
		if d.option == opt {
			return d.reason, true
		}
	}

	return "", false
}

// detectorOptions returns FilterOption values from the detectors table.
// If includeGeneric is true, all options are returned; otherwise FilterGeneric is excluded.
func detectorOptions(includeGeneric bool) []FilterOption {
	opts := make([]FilterOption, 0, len(detectors))

	for _, d := range detectors {
		if !includeGeneric && d.option == FilterGeneric {
			continue
		}

		opts = append(opts, d.option)
	}

	return opts
}

// matchesSQLCFilenamePattern checks if a base filename matches sqlc.dev naming patterns.
//
//nolint:gochecknoglobals // immutable lookup table, never mutated
var sqlcFilenamePatterns = []string{"models.go", "querier.go", "query.sql.go", "batch.go"}

func matchesSQLCFilenamePattern(filename string) bool {
	return slices.Contains(sqlcFilenamePatterns, filename) || strings.HasSuffix(filename, ".sql.go")
}

// IsSQLCGenerated checks if a file is generated by sqlc.dev.
func IsSQLCGenerated(filePath, content string) bool {
	if !matchesSQLCFilename(filePath) {
		return false
	}

	return hasSQLCContent(content)
}

// matchesSuffixPattern returns a function that matches filenames ending with the given suffix.
func matchesSuffixPattern(suffix string) func(string) bool {
	return func(f string) bool {
		return strings.HasSuffix(f, suffix)
	}
}

// codeGeneratedPrefix is the standard prefix for Go code generation comments.
// See https://go.dev/s/generatedcode for the specification.
const codeGeneratedPrefix = "// Code generated by"

// --- Content detection markers ---

const (
	sqlcGenerateComment = "// Code generated by sqlc. DO NOT EDIT."
	sqlcVersionBlock    = "versions:" // marker for sqlc version block
	sqlcAPIPrefix       = "sqlc "     // prefix for sqlc API calls

	templComponent = "templ.Component"
	templRenderSig = "Render(ctx context.Context, w io.Writer) error"

	goEnumComment = "// Code generated by go-enum"

	mockgenGeneratorName  = "MockGen"
	oapiCodegenMarker     = "oapi-codegen"
	deepcopyGeneratorName = "deepcopy-gen"
	wireGeneratorName     = "Wire"
	moqGeneratorName      = "moq"
	stringerGeneratorName = `"stringer"`

	mockeryMarker       = "Code generated by mockery"
	entMarker           = "Code generated by ent"
	entAltMarker        = "Code generated by entc"
	gqlgenMarker        = "Code generated by github.com/99designs/gqlgen"
	easyjsonMarker      = "Code generated by easyjson"
	msgpMarker          = "Code generated by github.com/tinylib/msgp"
	counterfeiterMarker = "Code generated by counterfeiter"
	goSwaggerMarker     = "Code generated by go-swagger"
)

