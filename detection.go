// DetectReason checks if a file is auto-generated based on filename and content.
// No I/O is performed — the caller provides the file content.
// Returns the reason for filtering, or ReasonNotFiltered if the file is not generated.
func DetectReason(filePath, content string, opts ...FilterOption) FilterReason {
	return detectReasonFromMap(filePath, content, optionsMap(opts...))
}

// DetectReasonFile checks if a file is auto-generated using two-phase detection.
// Phase 1 checks filename patterns (zero I/O). Phase 2 reads the file for content-based
// detection only if needed. This is the single-function equivalent of calling
// DetectReason with empty content, then reading the file and calling DetectReason again.
func DetectReasonFile(filePath string, opts ...FilterOption) (FilterReason, error) {
	return detectReasonFileFS(os.DirFS("."), filePath, opts...)
}

// DetectReasonFileFS checks if a file is auto-generated using two-phase detection
// with a custom filesystem. Phase 1 checks filename patterns (zero I/O).
// Phase 2 reads the file via fsys for content-based detection only if needed.
func DetectReasonFileFS(fsys fs.FS, filePath string, opts ...FilterOption) (FilterReason, error) {
	return detectReasonFileFS(fsys, filePath, opts...)
}

func detectReasonFileFS(fsys fs.FS, filePath string, opts ...FilterOption) (FilterReason, error) {
	options := optionsMap(opts...)

	reason := detectReasonFromMap(filePath, "", options)
	if reason != ReasonNotFiltered {
		return reason, nil
	}

	if !needsContentCheck(options) {
		return ReasonNotFiltered, nil
	}

	content, err := readFile(fsys, filePath)
	if err != nil {
		return ReasonNotFiltered, fmt.Errorf("read file %q: %w", filePath, err)
	}

	return detectReasonFromMap(filePath, string(content), options), nil
}

// DetectReasonReader checks if a file is auto-generated by reading content from an io.Reader.
// Useful when the caller already has the file content in a stream (e.g., from an HTTP handler
// or a processing pipeline) and wants to avoid reading it into a string first.
// Returns the reason for filtering, or ReasonNotFiltered if the file is not generated.
func DetectReasonReader(filePath string, r io.Reader, opts ...FilterOption) (FilterReason, error) {
	content, err := io.ReadAll(r)
	if err != nil {
		return ReasonNotFiltered, fmt.Errorf("read content from %q: %w", filePath, err)
	}

	return DetectReason(filePath, string(content), opts...), nil
}

// detectReasonFromMap is the shared core for both DetectReason and detectReasonFS.
func detectReasonFromMap(filePath, content string, options map[FilterOption]struct{}) FilterReason {
	// Phase 1: Check filename-based detectors (zero I/O)
	reason, _ := getFilenameBasedReasonWithTrace(filePath, options)
	if reason != ReasonNotFiltered {
		return reason
	}

	// Phase 2: Content-based detection only if content is provided and needed
	if content != "" && needsContentCheck(options) {
		reason, _ = getContentBasedReasonWithTrace(filePath, content, options)

		return reason
	}

	return ReasonNotFiltered
}

// optionsMap builds a lookup map from variadic FilterOption values.
// FilterAll is expanded to all specific options.
func optionsMap(opts ...FilterOption) map[FilterOption]struct{} {
	result := make(map[FilterOption]struct{}, len(opts))

	for _, opt := range opts {
		if opt == FilterAll {
			for _, specific := range detectorOptions(false) {
				result[specific] = struct{}{}
			}

			result[FilterGeneric] = struct{}{}
		} else {
			result[opt] = struct{}{}
		}
	}

	return result
}

// detectReasonFS determines if and why a file is auto-generated by reading it from the given filesystem.
// Two-phase detection: filename-based first (zero I/O), then content-based (reads file).
func detectReasonFS(
	fsys fs.FS,
	filePath string,
	options map[FilterOption]struct{},
) (FilterReason, error) {
	result, err := detectReasonFSWithTrace(fsys, filePath, options)

	return result.Reason, err
}

// needsContentCheck returns true if any enabled detector requires reading file content.
func needsContentCheck(options map[FilterOption]struct{}) bool {
	for i := range detectors {
		d := &detectors[i]
		if _, enabled := options[d.option]; enabled {
			if d.checkContent != nil {
				return true
			}
		}
	}

	return false
}

// getFilenameBasedReasonWithTrace is like getFilenameBasedReason but also returns a trace string.
func getFilenameBasedReasonWithTrace(
	filePath string,
	options map[FilterOption]struct{},
) (FilterReason, string) {
	filename := filepath.Base(filePath)

	for i := range detectors {
		d := &detectors[i]
		if _, enabled := options[d.option]; enabled {
			if d.matchFilename != nil && d.matchFilename(filename) {
				return d.reason, fmt.Sprintf("detected as %s via filename pattern", d.option)
			}
		}
	}

	return ReasonNotFiltered, ""
}

// getContentBasedReasonWithTrace checks content for generator-specific markers
// and returns both a reason and a trace string.
func getContentBasedReasonWithTrace(path string,
	fileContent string,
	opts map[FilterOption]struct{},
) (FilterReason, string) {
	for i := range detectors {
		d := &detectors[i]
		if _, enabled := opts[d.option]; enabled {
			if d.checkContent != nil && d.checkContent(path, fileContent) {
				trace := fmt.Sprintf("detected as %s via content marker", d.option)

				return d.reason, trace
			}
		}
	}

	return ReasonNotFiltered, ""
}

// detectReasonFSWithTrace is like detectReasonFS but returns a FilterResult with trace info.
func detectReasonFSWithTrace(
	fsys fs.FS,
	filePath string,
	options map[FilterOption]struct{},
) (FilterResult, error) {
	reason, trace := getFilenameBasedReasonWithTrace(filePath, options)
	if reason != ReasonNotFiltered {
		return FilterResult{Filtered: true, Reason: reason, Path: filePath, Trace: trace}, nil
	}

	if !needsContentCheck(options) {
		return FilterResult{
			Filtered: false,
			Reason:   ReasonNotFiltered,
			Path:     filePath,
			Trace:    "",
		}, nil
	}

	content, err := readFile(fsys, filePath)
	if err != nil {
		return FilterResult{
				Filtered: false,
				Reason:   ReasonNotFiltered,
				Path:     filePath,
				Trace:    "",
			}, fmt.Errorf(
				"read file %q: %w",
				filePath,
				err,
			)
	}

	reason, trace = getContentBasedReasonWithTrace(filePath, string(content), options)
	if reason != ReasonNotFiltered {
		return FilterResult{Filtered: true, Reason: reason, Path: filePath, Trace: trace}, nil
	}

	return FilterResult{
		Filtered: false,
		Reason:   ReasonNotFiltered,
		Path:     filePath,
		Trace:    "",
	}, nil
}

// AllFilterOptions returns all valid FilterOption values including the meta-option
// FilterAll. For enumerating only generator-specific options, use AllGeneratorOptions().
// Derived from the detectors table plus FilterAll, so adding a new detector
// automatically updates this list.
func AllFilterOptions() []FilterOption {
	opts := make([]FilterOption, 0, len(detectors)+1)

	for _, d := range detectors {
		opts = append(opts, d.option)
	}

	return append(opts, FilterAll)
}

// AllGeneratorOptions returns all FilterOption values that correspond to detectors
// in the table — including FilterGeneric but excluding the meta-option FilterAll.
// Use this when enumerating available detectors. Derived from the detectors table
// so adding a new detector automatically updates this list.
func AllGeneratorOptions() []FilterOption {
	return detectorOptions(true)
}

// AllFilterReasons returns all valid FilterReason values.
// Derived from the detectors table plus special reasons (include/exclude/not-filtered),
// so adding a new detector automatically updates this list.
func AllFilterReasons() []FilterReason {
	const nonDetectorReasons = 3

	reasons := make([]FilterReason, 0, len(detectors)+nonDetectorReasons)

	for _, d := range detectors {
		reasons = append(reasons, d.reason)
	}

	return append(reasons, ReasonOutsideScope, ReasonExcludePattern, ReasonNotFiltered)
}

// DetectorDoc provides documentation metadata for a detector.
// Used by gendocs (cmd/gendocs) to generate website data, README tables,
// and documentation pages from the single source of truth in the detectors table.
type DetectorDoc struct {
	Option            FilterOption // e.g., FilterSQLC
	Reason            FilterReason // e.g., ReasonSQLC
	URL               string       // generator homepage URL, "" if none
	FilenameDetection string       // human-readable filename pattern description
	ContentDetection  string       // human-readable content marker description
	IsFuncName        string       // exported Is*Generated function name
	HasFilenamePhase  bool         // true if filename-based detection exists
	HasContentPhase   bool         // true if content-based detection exists
}

// AllDetectorDocs returns documentation metadata for all detectors.
// Derived from the detectors table so adding a new detector automatically
// updates all generated documentation.
func AllDetectorDocs() []DetectorDoc {
	docs := make([]DetectorDoc, 0, len(detectors))

	for _, det := range detectors {
		docs = append(docs, DetectorDoc{
			Option:            det.option,
			Reason:            det.reason,
			URL:               det.url,
			FilenameDetection: det.filenameDesc,
			ContentDetection:  det.contentDesc,
			IsFuncName:        det.isFuncName,
			HasFilenamePhase:  det.matchFilename != nil,
			HasContentPhase:   det.checkContent != nil,
		})
	}

	return docs
}

// readFile reads file content, falling back to os.ReadFile for absolute paths
// when fs.ReadFile fails (os.DirFS rejects absolute paths).
func readFile(fsys fs.FS, filePath string) ([]byte, error) {
	content, err := fs.ReadFile(fsys, filePath)
	if err == nil {
		return content, nil
	}

	if filepath.IsAbs(filePath) {
		//nolint:gosec // G304: filePath is user-provided by design — library reads from fs.FS
		return os.ReadFile(filePath) //nolint:wrapcheck // caller wraps with context
	}

	return nil, fmt.Errorf("read file from fs: %w", err)
}
