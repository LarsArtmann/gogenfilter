// detector represents a code generator with optional filename and content detection.
// matchFilename is used in phase 1 (zero I/O); checkContent in phase 2 (reads file).
// The reason field explicitly maps each FilterOption to its FilterReason,
// eliminating the implicit string-value coupling between the two types.
// The url, filenameDesc, contentDesc, and isFuncName fields provide documentation metadata
// consumed by gendocs (cmd/gendocs) to generate website and README tables.
type detector struct {
	option        FilterOption
	reason        FilterReason
	matchFilename func(string) bool
	checkContent  func(string, string) bool
	url           string // generator homepage URL, "" if none
	filenameDesc  string // human-readable filename pattern description
	contentDesc   string // human-readable content marker description
	isFuncName    string // exported Is*Generated function name, e.g., "IsSQLCGenerated"
}

