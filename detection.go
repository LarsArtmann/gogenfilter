// anyMatch returns true if the predicate returns true for any element.
func anyMatch(s string, candidates []string, pred func(string, string) bool) bool {
	return slices.ContainsFunc(candidates, func(c string) bool {
		return pred(s, c)
	})
}

func matchesProtobufFilename(filename string) bool {
	return anyMatch(filename, protobufFilenameSuffixes, strings.HasSuffix)
}

// matchesMockgenFilename checks if a base filename matches mockgen naming patterns.
func matchesMockgenFilename(filename string) bool {
	return strings.HasSuffix(filename, "_mock.go")
}

// matchesSQLCFilename checks if filename matches sqlc.dev naming patterns.
func matchesSQLCFilename(filePath string) bool {
	return matchesSQLCFilenamePattern(filepath.Base(filePath))
}

// hasSQLCContent checks if content contains sqlc.dev specific markers.
func hasSQLCContent(content string) bool {
	if strings.Contains(content, sqlcGenerateComment) {
		return true
	}

	if strings.Contains(content, sqlcAPIPrefix) && strings.Contains(content, sqlcVersionBlock) {
		return true
	}

	return hasSQLCCodePatterns(content)
}

// hasSQLCCodePatterns checks for sqlc-specific code patterns in content.
func hasSQLCCodePatterns(content string) bool {
	return anyMatch(content, sqlcCodePatternMarkers, strings.Contains)
}

// IsTemplGenerated checks if a file is generated by templ.guide.
func IsTemplGenerated(filePath, content string) bool {
	filename := filepath.Base(filePath)

	if !strings.HasSuffix(filename, "_templ.go") {
		return false
	}

	if strings.Contains(content, templComponent) {
		return true
	}

	if strings.Contains(content, templRenderSig) {
		return true
	}

	return false
}

