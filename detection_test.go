func detectorPriorityTests() []struct {
	name     string
	path     string
	content  string
	expected FilterReason
} {
	return []struct {
		name     string
		path     string
		content  string
		expected FilterReason
	}{
		{
			name:     "stringer beats generic",
			path:     "status_string.go",
			content:  "// Code generated by \"stringer\". DO NOT EDIT.\npackage enums\n",
			expected: ReasonStringer,
		},
		{
			name:     "sqlc beats generic",
			path:     "db/models.go",
			content:  "// Code generated by sqlc. DO NOT EDIT.\npackage db\n",
			expected: ReasonSQLC,
		},
		{
			name:     "mockgen beats generic",
			path:     "service_mock.go",
			content:  "// Code generated by MockGen. DO NOT EDIT.\npackage mocks\n",
			expected: ReasonMockgen,
		},
		{
			name:     "generic catches unknown tool",
			path:     "custom_gen.go",
			content:  "// Code generated by my-tool. DO NOT EDIT.\npackage main\n",
			expected: ReasonGeneric,
		},
		{
			name:     "go-enum beats generic",
			path:     "status_enum.go",
			content:  "// Code generated by go-enum DO NOT EDIT.\npackage enums\n",
			expected: ReasonGoEnum,
		},
		{
			name:     "protobuf beats generic",
			path:     "user.pb.go",
			content:  "// Code generated by protoc-gen-go. DO NOT EDIT.\npackage pb\n",
			expected: ReasonProtobuf,
		},
	}
}

