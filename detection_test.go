func sqlcPatternAndContentTests() ([]boolTestCase[string], []boolTestCase[string]) {
	codePatternTests := make([]boolTestCase[string], 0, 4)
	codePatternTests = addBoolTestCase(addBoolTestCase(addBoolTestCase(addBoolTestCase(
		codePatternTests, "sqlc.Arg", "id := sqlc.Arg(\"user_id\")", true), "sqlc.Narg", "id := sqlc.Narg(\"user_id\")", true), "query method", "row := q.query(ctx, sql)", true), "no pattern", "package main\nfunc main() {}", false)

	contentTests := make([]boolTestCase[string], 0, 4)
	contentTests = addBoolTestCase(addBoolTestCase(addBoolTestCase(addBoolTestCase(
		contentTests, "code generated comment", "// Code generated by sqlc. DO NOT EDIT.\npackage db\n", true), "sqlc versions block", "// Code generated by sqlc v1.25.0\n// versions:\npackage db\n", true), "code pattern only", "package db\nid := sqlc.Arg(\"id\")\n", true), "no sqlc markers", "package main\nfunc main() {}\n", false)

	return codePatternTests, contentTests
}

