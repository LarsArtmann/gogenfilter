func TestDetectReasonNoIO(t *testing.T) {
	t.Parallel()

	t.Run("detects sqlc from content without I/O", func(t *testing.T) {
		t.Parallel()

		content := "// Code generated by sqlc. DO NOT EDIT.\npackage db\ntype User struct{}\n"
		testDetectReason(t, "models.go", content, map[FilterOption]bool{FilterSQLC: true}, ReasonSQLC)
	})

	t.Run("detects generic from content without I/O", func(t *testing.T) {
		t.Parallel()

		content := "// Code generated by foobar. DO NOT EDIT.\npackage main\n"
		testDetectReason(t, "foo.go", content, map[FilterOption]bool{FilterGeneric: true}, ReasonGeneric)
	})

	t.Run("returns not filtered for non-matching content", func(t *testing.T) {
		t.Parallel()
		testDetectReasonNotFiltered(t, "file.go", "package main", map[FilterOption]bool{FilterGeneric: true})
	})
}

