func sqlcPatternAndContentTests() ([]boolTestCase[string], []boolTestCase[string]) {
	codePatternTests := make([]boolTestCase[string], 0, 4)
	codePatternTests = append(codePatternTests,
		boolTestCase[string]{"sqlc.Arg", "id := sqlc.Arg(\"user_id\")", true},
		boolTestCase[string]{"sqlc.Narg", "id := sqlc.Narg(\"user_id\")", true},
		boolTestCase[string]{"query method", "row := q.query(ctx, sql)", true},
		boolTestCase[string]{"no pattern", "package main\nfunc main() {}", false},
	)

	contentTests := make([]boolTestCase[string], 0, 4)
	contentTests = append(contentTests,
		boolTestCase[string]{
			"code generated comment",
			"// Code generated by sqlc. DO NOT EDIT.\npackage db\n",
			true,
		},
		boolTestCase[string]{
			"sqlc versions block",
			"// Code generated by sqlc v1.25.0\n// versions:\npackage db\n",
			true,
		},
		boolTestCase[string]{
			"code pattern only",
			"package db\nid := sqlc.Arg(\"id\")\n",
			true,
		},
		boolTestCase[string]{
			"no sqlc markers",
			"package main\nfunc main() {}\n",
			false,
		},
	)

	return codePatternTests, contentTests
}

func TestSQLCDetection(t *testing.T) {
	t.Parallel()

	codePatternTests, contentTests := sqlcPatternAndContentTests()

	t.Run("HasSQLCCodePatterns", func(t *testing.T) {
		t.Parallel()
		runBoolTableTest(t, codePatternTests, HasSQLCCodePatterns, "HasSQLCCodePatterns")
	})

	t.Run("HasSQLCContent", func(t *testing.T) {
		t.Parallel()
		runBoolTableTest(t, contentTests, HasSQLCContent, "HasSQLCContent")
	})
}
