func ExampleWithIncludePatterns() {
	mapFS := fstest.MapFS{
		"db/models.go": &fstest.MapFile{
			Data: []byte("// Code generated by sqlc. DO NOT EDIT.\npackage db\n"),
		},
		"other/file.go": &fstest.MapFile{
			Data: []byte("// Code generated by sqlc. DO NOT EDIT.\npackage other\n"),
		},
	}

	filter := gogenfilter.NewFilter(
		gogenfilter.Enabled(),
		gogenfilter.WithFilterOptions(gogenfilter.FilterSQLC),
		gogenfilter.WithIncludePatterns("db/**"),
		gogenfilter.WithFS(mapFS),
	)

	dbFiltered, _ := filter.ShouldFilter("db/models.go")
	otherFiltered, _ := filter.ShouldFilter("other/file.go")
	fmt.Println(dbFiltered, otherFiltered)
	// Output: true true
}

