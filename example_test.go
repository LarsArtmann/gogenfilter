func ExampleWithExcludePatterns_vendorAndTestdata() {
	sqlcContent := sqlcDBContent
	mapFS := mapFSWithFiles(map[string]string{
		"vendor/github.com/foo/bar.go": "package foo\n",
		"testdata/models.go":           sqlcContent,
		"db/models.go":                 sqlcContent,
		"main.go":                      "package main\n",
	})

	opts, err := gogenfilter.WithFilterOptions(gogenfilter.FilterSQLC)
	if err != nil {
		return
	}

	filter, err := gogenfilter.NewFilter(
		opts,
		gogenfilter.WithExcludePatterns("vendor/**", "**/testdata/**"),
		gogenfilter.WithFS(mapFS),
	)
	if err != nil {
		return
	}

	vendorFiltered, _ := filter.Filter("vendor/github.com/foo/bar.go")
	testdataFiltered, _ := filter.Filter("testdata/models.go")
	dbFiltered, _ := filter.Filter("db/models.go")
	mainFiltered, _ := filter.Filter("main.go")

	fmt.Println(vendorFiltered, testdataFiltered, dbFiltered, mainFiltered)
	// Output: true true true false
}

