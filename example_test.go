func ExampleFilter_GetStats() {
	mapFS := fstest.MapFS{
		"db/models.go": &fstest.MapFile{
			Data: []byte("// Code generated by sqlc. DO NOT EDIT.\npackage db\n"),
		},
		"main.go": &fstest.MapFile{
			Data: []byte("package main\n"),
		},
	}

	filter := gogenfilter.NewFilter(
		gogenfilter.Enabled(),
		gogenfilter.WithFilterOptions(gogenfilter.FilterSQLC),
		gogenfilter.WithFS(mapFS),
	)

	filter.ShouldFilter("db/models.go")
	filter.ShouldFilter("main.go")

	stats := filter.GetStats()
	fmt.Println(stats.TotalFilesChecked)
	fmt.Println(stats.FilteredBy(gogenfilter.ReasonSQLC))
	fmt.Println(stats.TotalFiltered())
	// Output:
	// 2
	// 1
	// 1
}

