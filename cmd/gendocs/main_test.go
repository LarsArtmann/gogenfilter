func TestValidateMetadataMissingEntry(t *testing.T) {
	t.Parallel()

	docs := gogenfilter.AllDetectorDocs()
	docs = append(docs, gogenfilter.DetectorDoc{ //nolint:exhaustruct
		Option:     "nonexistent",
		Reason:     "nonexistent",
		IsFuncName: "IsNonexistentGenerated",
	})

	err := validateMetadata(docs)
	if err == nil {
		t.Fatal("validateMetadata() should fail for missing website metadata")
	}

	if !strings.Contains(err.Error(), "nonexistent") {
		t.Errorf("validateMetadata() error should mention the missing detector, got: %v", err)
	}
}

