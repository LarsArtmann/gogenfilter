func TestOptionToConstName(t *testing.T) {
	t.Parallel()

	//nolint:goconst // test data, not production constants
	tests := []struct {
		option string
		want   string
	}{
		{"sqlc", "FilterSQLC"},
		{"oapi-codegen", "FilterOapi"},
		{"deepcopy-gen", "FilterDeepcopy"},
		{"go-enum", "FilterGoEnum"},
		{"go-swagger", "FilterGoSwagger"},
		{"templ", "FilterTempl"},
		{"wire", "FilterWire"},
		{"generic", "FilterGeneric"},
	}

	for _, testCase := range tests {
		t.Run(testCase.option, func(t *testing.T) {
			t.Parallel()

			got := optionToConstName(testCase.option)
			if got != testCase.want {
				t.Errorf("optionToConstName(%q) = %q, want %q", testCase.option, got, testCase.want)
			}
		})
	}
}

