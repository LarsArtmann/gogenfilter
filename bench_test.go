func BenchmarkMatchPattern(b *testing.B) {
	tests := []struct {
		name    string
		path    string
		pattern string
	}{
		{name: "exact", path: "file.go", pattern: "file.go"},
		{name: "wildcard", path: "test.go", pattern: "*.go"},
		{name: "doublestar", path: "a/b/c/file.go", pattern: "**/*.go"},
		{name: "question", path: "a.go", pattern: "?.go"},
		{name: "no_match", path: "other.txt", pattern: "*.go"},
	}

	for _, tt := range tests {
		b.Run(tt.name, func(b *testing.B) {
			for b.Loop() {
				_ = MatchPattern(tt.path, tt.pattern)
			}
		})
	}
}

func BenchmarkDetectReason(b *testing.B) {
	allOpts := map[FilterOption]bool{FilterAll: true}

	tests := []struct {
		name    string
		path    string
		content string
	}{
		{
			name:    "filename_only",
			path:    "db/models.go",
			content: "// Code generated by sqlc. DO NOT EDIT.\npackage db\n",
		},
		{
			name:    "content_based",
			path:    "custom_gen.go",
			content: "// Code generated by my-tool. DO NOT EDIT.\npackage main\n",
		},
	}

	for _, tt := range tests {
		b.Run(tt.name, func(b *testing.B) {
			for b.Loop() {
				_ = DetectReason(tt.path, tt.content, allOpts)
			}
		})
	}

	b.Run("not_filtered", func(b *testing.B) {
		for b.Loop() {
			_ = DetectReason("main.go", "package main\n", allOpts)
		}
	})
}
